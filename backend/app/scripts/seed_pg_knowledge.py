async def seed() -> None:
    """Seed all PG knowledge base articles into the database.

    Skips articles whose titles already exist to support idempotent re-runs.
    Embedding generation via Ollama is triggered inside create_article; if
    Ollama is unavailable the article is still persisted without a vector.
    """
    async with AsyncSessionLocal() as db:
        print("--- PG Knowledge Base Seed Starting ---")
        seeded = 0
        skipped = 0

        for article_dict in ARTICLES:
            # Skip duplicates by title
            existing = await db.execute(
                select(KnowledgeArticle).where(
                    KnowledgeArticle.title == article_dict["title"]
                )
            )
            if existing.scalar_one_or_none() is not None:
                print(f"  [SKIP] {article_dict['title']}")
                skipped += 1
                continue

            payload = KnowledgeArticleCreate(
                title=article_dict["title"],
                category=article_dict["category"],
                content=article_dict["content"],
                tags=article_dict["tags"],
            )
            try:
                await create_article(db, payload, created_by=None)
                await db.commit()
                print(f"  [OK] {article_dict['title']}")
                seeded += 1
            except Exception as exc:  # noqa: BLE001
                await db.rollback()
                print(f"  [FAIL] {article_dict['title']}: {exc}")

        print(
            f"\n=== PG Knowledge Base Seed Complete ==="
            f"\n  Total articles defined : {len(ARTICLES)}"
            f"\n  Seeded                 : {seeded}"
            f"\n  Skipped (already exist): {skipped}"
        )


if __name__ == "__main__":
    asyncio.run(seed())
